  PORT(SW     : IN std_logic_vector(4 DOWNTO 0); --switch inputs 0-5... 5 OPTIONS
       S : OUT std_logic_vector(27 DOWNTO 0)); -- TOTAL OF 4 LETTERS OR NUMBERS DISPLAYED
END project;
ARCHITECTURE behavior OF project IS
	
			
