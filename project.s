	S <="0001100000100000110010101100" WHEN SW="00000" else -- not paid
	"0001100000100000110010101100" WHEN SW="00001" else -- not paid
	"0001100000100000110010101100" WHEN SW="00010" else -- not paid
	"0001100000100000110010101100" WHEN SW="00100" else -- not paid
	"0001100000100000110010101100" WHEN SW="01000" else -- not paid
	"0001100000100010011110100001" WHEN SW="10000" else -- paid
	"1000110100000000010100000110" WHEN SW="10001" else -- SW0 IS SODA COKE
	"0100001010111100011000001100" WHEN SW="10010" else --SW1 IS SODA DR.PEPPER
	"0010010000110001011111001110" WHEN SW="10100" else --SW2 IS SODA SPRITE
	"1111000011111110000010001100" WHEN SW="11000" else --SW3 IS SODA 7-UP
	"0010010100111010000000001100" ; --STOP displays if more than one drink is selected
			END behavior;
